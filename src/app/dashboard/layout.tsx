export default function DashboardLayout({ children }: { children: ReactNode }) {
  const pathname = usePathname();
  const { user } = useAuth();
  const [role, setRole] = useState<string>("user");

  useEffect(() => {
    if (!user) return;
    const supabase = getBrowserSupabase();
    if (!supabase) return;

    supabase
      .from("profiles")
      .select("role")
      .eq("id", user.id)
      .single()
      .then(({ data }) => {
        if (data?.role) setRole(data.role);
      });
  }, [user]);

  const items = [
    ...userItems,
    ...(role === "broker" || role === "admin" ? [{ divider: true as const, label: "Makléř" }] : []),
    ...(role === "broker" || role === "admin" ? brokerItems : []),
    ...(role === "admin" ? [{ divider: true as const, label: "Administrace" }] : []),
    ...(role === "admin" ? adminItems : []),
  ];

  return (
    <div className="page-shell">
      <SiteHeader />
      <div className="dashboard-layout">
        <aside className="dashboard-sidebar">
          <nav className="dashboard-nav">
            {items.map((item, i) => {
              if ("divider" in item) {
                return (
                  <div key={`divider-${i}`} className="dashboard-nav-divider">
                    <span>{item.label}</span>
                  </div>
                );
              }
              const isActive =
                item.href === "/dashboard"
                  ? pathname === "/dashboard"
                  : pathname.startsWith(item.href);
              return (
                <Link
                  key={item.href}
                  href={item.href}
                  className={`dashboard-nav-item ${isActive ? "dashboard-nav-item--active" : ""}`}
                >
                  {item.icon}
                  <span>{item.label}</span>
                </Link>
              );
            })}
          </nav>
        </aside>
        <main className="dashboard-content">{children}</main>
      </div>
    </div>
  );
}
